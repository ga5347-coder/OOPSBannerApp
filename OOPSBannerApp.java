/**
 * OOPSBannerApp UC2 - Banner Display Application
 *
 * Displays the word "OOPS" in a 7-line ASCII banner
 * using asterisks (*) and spaces.
 *
 * @author Gayathri
 * @version 2
 */

public class OOPSBannerApp {

    public static void main(String[] args) {

        // Line 1
        System.out.println("  *****  " + "  *****  " + "  ****** " + "  ****** ");

        // Line 2
        System.out.println(" *     * " + " *     * " + " *     * " + " *      ");

        // Line 3
        System.out.println("*       *" + "*       *" + "*     *  " + "*      ");

        // Line 4
        System.out.println("*       *" + "*       *" + "******   " + " *****  ");

        // Line 5
        System.out.println("*       *" + "*       *" + "*        " + "      * ");

        // Line 6
        System.out.println(" *     * " + " *     * " + " *       " + " *    *  ");

        // Line 7
        System.out.println("  *****  " + "  *****  " + " *        " + "  ****   ");

    }
}
